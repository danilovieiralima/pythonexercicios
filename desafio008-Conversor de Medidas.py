metro = float(input('Digite um valor em metros: '))
decim = metro * 10
cent = metro * 100
mili = metro * 1000
quilom = metro / 1000
hectom = metro / 100
decam = metro / 10
print('O valor em decímetros é {}dm'.format(decim))
print('O valor em centímetros é {}cm'.format(cent))
print('O valor em milímetros é {}mm'.format(mili))
print('O valor em quilometro é {}km'.format(quilom))
print('O valor em hectometro é {}hm'.format(hectom))
print('O valor em decâmetro é {}dam'.format(decam))
