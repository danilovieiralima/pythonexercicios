def dobro(preco):
    preco2 = preco * 2
    return preco2


def metade(preco):
    preco2 = preco / 2
    return preco2


def aumentar(preco, taxa):
    preco2 = preco + (preco * taxa / 100)
    return preco2


def diminuir(preco, taxa):
    preco2 = preco - (preco * taxa / 100)
    return preco2

